    private void showStudent(){
        studentl = ConJDBC.findAll();
        
        tableModel.setRowCount(0);
        
        studentl.forEach((st)->{
            tableModel.addRow(new Object[] {tableModel.getRowCount() + 1, st.getFullname(), st.getGender(),st.getAge(), st.getEmail(),st.getPhoneNumber()});
        });
    }

