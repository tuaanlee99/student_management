    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        String fullname = txtFullName.getText();
        String gender = jcbGender.getSelectedItem().toString();
        int age = Integer.parseInt(txtAge.getText());
        String email = txtEmail.getText();
        String phoneNumber = txtPhoneNumber.getText();
        
        Student st = new Student(fullname, gender, email, phoneNumber, age);
        ConJDBC.insert(st);
        
        showStudent();
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeleteActionPerformed
        // TODO add your handling code here:
        int selectedIndex = tblStudent.getSelectedRow();
        if(selectedIndex>=0){
            Student st = studentl.get(selectedIndex);
            
            int option = JOptionPane.showConfirmDialog(this, "Do yo want delete this student?");
            System.err.println("option" + option);
            
            if(option == 0){
                ConJDBC.delete(st.getId());
                
                showStudent();
            }
        }
    }//GEN-LAST:event_btndeleteActionPerformed

    private void btnFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFindActionPerformed
        // TODO add your handling code here:
        String input = JOptionPane.showInputDialog(this, "Enter full name to search");
        if(input !=null &&input.length()>0){
            studentl = ConJDBC.findByName(input);
            
            tableModel.setRowCount(0);
            
            studentl.forEach((st)->{
                tableModel.addRow(new Object[] {tableModel.getRowCount()+1, st.getId(),st.getFullname(),st.getGender(),st.getAge(),st.getEmail(),st.getPhoneNumber()});
            });
        } else {
            showStudent();
        }
    }//GEN-LAST:event_btnFindActionPerformed

